// ========================================
// Print Statements (Console Output)
// ========================================
// In TypeScript/JavaScript, we use console.log() to print output to the console

// Basic print statement
console.log("Hello, World!");

// Print multiple things on separate lines
console.log("Welcome to TypeScript!");
console.log("This is lesson 1");

// Print numbers
console.log(42);
console.log(3.14);

// Print results of calculations
console.log(5 + 3);
console.log(10 - 2);
console.log(4 * 2);

// Print multiple items on one line (separated by spaces)
console.log("The answer is:", 42);
console.log("Sum:", 5, "+", 3, "=", 8);


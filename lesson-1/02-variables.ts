// ========================================
// Variables
// ========================================
// Variables are containers that store data values
// In TypeScript, we use 'let' to create variables that can change
// and 'const' for variables that stay the same

// Creating variables with 'let' (can be changed)
let userName = "Alice";
let age = 25;
let isStudent = true;

console.log("Name:", userName);
console.log("Age:", age);
console.log("Is student:", isStudent);

// Changing variable values
age = 26;
console.log("New age:", age);

userName = "Bob";
console.log("New name:", userName);

// Creating constants with 'const' (cannot be changed)
const pi = 3.14159;
const daysInWeek = 7;

console.log("Pi:", pi);
console.log("Days in a week:", daysInWeek);

// Variables in calculations
let apples = 5;
let oranges = 3;
let totalFruit = apples + oranges;

console.log("Total fruit:", totalFruit);

// Using variables in strings
let greeting = "Hello";
let personName = "Charlie";
console.log(greeting + ", " + personName + "!");

